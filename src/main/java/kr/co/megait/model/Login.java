package kr.co.megait.model;

public class Login {

	private int login_idx;
	private int member_idx;
	private String login_time;
	private String login_check_yn;
	
	
	
	public int getLogin_idx() {
		return login_idx;
	}
	public void setLogin_idx(int login_idx) {
		this.login_idx = login_idx;
	}
	
	public int getMember_idx() {
		return member_idx;
	}
	public void setMember_idx(int member_idx) {
		this.member_idx = member_idx;
	}
	
	public String getLogin_time() {
		return login_time;
	}
	public void setLogin_time(String login_time) {
		this.login_time = login_time;
	}
	
	public String getLogin_check_yn() {
		return login_check_yn;
	}
	public void setLogin_check_yn(String login_check_yn) {
		this.login_check_yn = login_check_yn;
	}
	
	
	
	
	
	
	
}
